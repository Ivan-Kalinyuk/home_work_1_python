X = int(input('1. Введите X (0 или 1) : '))
Y = int(input('2. Введите Y (0 или 1) : '))
Z = int(input('3. Введите Z (0 или 1) : '))
if X == 0 and Y == 0 and Z == 0:
    print (f'не ({X} или {Y} или {Z}) = 1 или же не {X} и не {Y} и не {Z}  = 1 ')
else:
    print (f'не ({X} или {Y} или {Z}) = 0 или же не {X} и не {Y} и не {Z}  = 0 ')
